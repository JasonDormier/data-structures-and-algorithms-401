function todayBinary(arr, key) {
  //return index of found item or -1
  // use iteration
  let start = 0;
  let end = arr.length
  while (start <= end) {
    let mid = Math.floor((start + end) / 2);
    if (arr[mid] === key) return mid;

    if (key < arr[mid]) end = mid - 1;
    else start = mid + 1;
  }
  return -1;
}

